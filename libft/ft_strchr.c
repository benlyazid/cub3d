#include "libft.h"

char	*ft_strchr(const char *src, int d)
{
	char *s;

	s = (char*)src;
	while (*s != '\0')
	{
		if (*s == d)
			return (s);
		s++;
	}
	if (d == 0)
		return (s);
	return (0);
}
