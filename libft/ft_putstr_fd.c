#include "libft.h"

#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int i;

	if (s != 0 && fd > -1)
	{
		i = 0;
		while (s[i])
		{
			write(fd, &(s[i]), 1);
			i++;
		}
	}
}
