#include "libft.h"

char	*ft_strrchr(const char *src, int d)
{
	char *s;

	s = 0;
	while (*src != '\0')
	{
		if (*src == d)
			s = (char*)src;
		src++;
	}
	if (d == '\0')
		s = (char*)src;
	return (s);
}
